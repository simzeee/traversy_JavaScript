const id = 100;

//Equal to
if (id == 100){
  console.log('correct')
} else {
  console.log('incorrect')
}

if (id != 100){
  console.log('correct')
} else {
  console.log('incorrect')
}

//  Equal to value & type

if (id === 100){
  console.log('correct')
} else {
  console.log('incorrect')
}

if (id !== 100){
  console.log('correct')
} else {
  console.log('incorrect')
}


// test if undefined
if (typeof id !== 'undefined'){
  console.log('correct')
} else {
  console.log('incorrect')
}

